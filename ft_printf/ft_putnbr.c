#include "ft_printf.h"

int	ft_putnbr(long n)
{
	int	r;

	r = 0;
	if (n < 0)
	{
		n *= -1;
		r += write(1, "-", 1);
	}
	if (n > 9)
	{
		r += ft_putnbr(n / 10);
		r += ft_putchar((n % 10) + 48);
	}
	else
		r += ft_putchar(n + 48);
	return (r);
}
